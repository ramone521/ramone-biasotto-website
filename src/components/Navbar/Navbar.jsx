import React, { useState, useEffect, useRef } from "react";
import { useLocation, Link, useNavigate } from "react-router-dom";
import styles from "./styles.module.scss";
import { wppDefault } from "../../utils/whatsapp";

const Navbar = () => {
  const location = useLocation();
  const navigate = useNavigate();
  const isSolid = location.pathname !== "/";
  const [menuOpen, setMenuOpen] = useState(false);
  const [scrolled, setScrolled] = useState(false);
  const pendingHash = useRef(null);

  useEffect(() => {
    const handleScroll = () => {
      setScrolled(window.scrollY > 20);
    };

    handleScroll();
    window.addEventListener("scroll", handleScroll, { passive: true });
    return () => window.removeEventListener("scroll", handleScroll);
  }, []);

  useEffect(() => {
    if (menuOpen) {
      document.body.style.overflow = "hidden";
    } else {
      document.body.style.overflow = "";
    }
    return () => {
      document.body.style.overflow = "";
    };
  }, [menuOpen]);

  useEffect(() => {
    if (location.pathname === "/" && pendingHash.current) {
      const hash = pendingHash.current;
      pendingHash.current = null;
      setTimeout(() => {
        const el = document.getElementById(hash);
        if (el) el.scrollIntoView({ behavior: "smooth" });
      }, 80);
    }
  }, [location.pathname]);

  const handleLinkClick = () => {
    setMenuOpen(false);
  };

  const handleNavClick = (e, hash) => {
    if (isSolid) {
      e.preventDefault();
      pendingHash.current = hash;
      setMenuOpen(false);
      navigate("/");
    }
  };

  const anchors = [
    { hash: "sobre", label: "Sobre" },
    { hash: "areas", label: "Áreas de Atuação" },
    { hash: "servicos", label: "Serviços" },
    { hash: "midia", label: "Na mídia" },
    { hash: "livros", label: "Livros & E-books" },
    { hash: "contato", label: "Contato" },
  ];

  return (
    <>
      <header
        className={`${styles.containerHeader} ${
          scrolled ? styles.scrolled : ""
        } ${isSolid ? styles.solid : ""}`}
      >
        <div className={styles.containerContentHeader}>
          <button
            className={`${styles.hamburgerBtn} ${
              menuOpen ? styles.active : ""
            }`}
            onClick={() => setMenuOpen(!menuOpen)}
            aria-label="Abrir menu"
            aria-expanded={menuOpen}
            type="button"
          >
            <MenuIcon />
          </button>

          <Link to="/" className={styles.containerLogo}>
            <LogoCinza />
            <h1>RAMONE BIASOTTO ADVOCACIA</h1>
          </Link>

          <div className={styles.containerLinks}>
            {anchors.map((anchor, i) => (
              <a
                key={i}
                href={isSolid ? `/#${anchor.hash}` : `#${anchor.hash}`}
                onClick={(e) => handleNavClick(e, anchor.hash)}
              >
                {anchor.label}
              </a>
            ))}
          </div>

          <a
            href={wppDefault}
            target="_blank"
            rel="noopener noreferrer"
            className={styles.contactBtn}
            aria-label="Fale pelo WhatsApp"
          >
            <WhatsappIcon />
          </a>
        </div>
      </header>

      <div
        className={`${styles.overlay} ${menuOpen ? styles.overlayActive : ""}`}
        onClick={() => setMenuOpen(false)}
      ></div>

      <nav
        className={`${styles.mobileDrawer} ${
          menuOpen ? styles.drawerOpen : ""
        }`}
        inert={!menuOpen}
      >
        <div className={styles.drawerHeader}>
          <Link to="/" className={styles.drawerLogo} onClick={handleLinkClick}>
            <LogoCinza />
            <span>RAMONE BIASOTTO</span>
          </Link>
          <button
            className={styles.closeBtn}
            onClick={() => setMenuOpen(false)}
            aria-label="Fechar menu"
            type="button"
          >
            <CloseIcon />
          </button>
        </div>

        <div className={styles.drawerLinks}>
          {anchors.map((anchor, i) => (
            <a
              key={i}
              href={isSolid ? `/#${anchor.hash}` : `#${anchor.hash}`}
              onClick={(e) => {
                handleNavClick(e, anchor.hash);
                handleLinkClick();
              }}
            >
              {anchor.label}
            </a>
          ))}
        </div>
      </nav>
    </>
  );
};

export default Navbar;

